            <h1>Sadhana</h1>
            <h2>Academy Proceedings in Engineering Sciences</h2>
            <h3>History</h1>
            <p>Sadhana – Academy Proceedings in Engineering Sciences was started in the year 1978 after it split from being a part of the  Proceedings of the Indian Academy of Sciences – Section C, and was renamed Sadhana in the year 1984.  The word Sadhana  from Sanskrit will be familiar to Indian readers. Apte's well-known dictionary lists among its meanings “accomplishment …proof…instrument or tool..”  –  all conveying the sense of physical achievement that is central to successful engineering.</p>
            <h3>Aim and Scope</h3>
            <p>The journal covers all branches of engineering science and technology including mechanics (fluid, solid, thermal), computer science,   electronics, energy, aerospace technology, materials science, nuclear engineering, systems analysis, alternative technologies, etc.  The journal focuses on papers that are relevant to more than one professional group, either because the work is fundamental or because it reflects the best in current technology; also on summaries of special projects of interest to engineering scientists. The journal also encourages critical reviews, especially on subjects of interest to the country.  Papers on developments in Indian technology must pay special attention to the problems peculiar to the country in such projects.</p>
