            <h3>History</h1>
            <p>Resonance – Journal of Science Education started in the year 1996 was the culmination of intensive efforts by the Academy through a panel which studied the state of science education in the country. The journal’s objective therefore is targeted primarily at science education for undergraduate students and teachers and focuses on enriching the processes of teaching and learning science thereby stimulating science education in the country.</p>
            <h3>Aim and Scope</h3>
            <p>The journal invites articles in various branches of science --  physics, chemistry, biology, mathematics, computer science and engineering and emphasizes on a lucid style that will attract readers from diverse backgrounds. Articles on topics within the undergraduate curriculum, especially those that students often consider difficult to understand, new classroom experiments, emerging techniques and ideas, and innovative procedures for teaching specific concepts are particularly welcome.  The articles are of various categories: general articles, series articles, concise texts in article-in-a-box, classroom and nature-watch, research news, book reviews, face-to-face;theme articles in ‘Classics’ or ‘Reflections’ and information and announcements useful to students and teachers. Each issue highlights the contributions of an important scientist.</p>
