            <h2>&nbsp;</h2>
            <h3>History</h1>
            <p>The Journal of Earth System Science was earlier a part of  the Proceedings of the Indian Academy of Sciences – Section A begun in 1934,  and later split in 1978 into theme journals. This journal was published as Proceedings – Earth and Planetary Sciences since 1978, and in 2005 was renamed ‘Journal of Earth System Science’.</p>
            <h3>Aim and Scope</h3>
            <p>The journal is highly interdisciplinary and publishes high-quality research – new data, ideas, and conceptual advances – in earth system science in its broadest sense. This includes the solid earth, the atmosphere, the hydrosphere, and the biosphere; it also addresses related aspects of planetary and space sciences. Papers pertaining to the Indian subcontinent and the surrounding Indian Ocean region are particularly welcome.</p>
