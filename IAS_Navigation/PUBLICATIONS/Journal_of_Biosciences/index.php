            <h3>History</h1>
            <p>The <em>Journal of Biosciences</em> began in 1934 as a part of the <em>Proceedings of the Indian Academy of Sciences – Section B</em> until it was split in 1978 from Proceedings – Section B to become Proceedings – Animal Sciences, Proceedings --  Plant Sciences and Proceedings – Experimental Biology. In 1979 Proceedings -- Experimental Biology  was named Journal of Biosciences. In  1991,  Animal Sciences and Plant Sciences merged with experimental Biology to comprise one journal and retained the name Journal of Biosciences.</p>
            <h3>Aim and Scope</h3>
            <p>The journal covers all areas of biology and is considered the premier journal in the country within its scope. It publishes research and reviews, series articles, perspectives, clipboards, commentaries, and short communications. Special theme issues are also published.</p>
