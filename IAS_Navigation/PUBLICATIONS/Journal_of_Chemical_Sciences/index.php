            <h3>History</h1>
            <p>Originally a  part of the <em>Proceedings of the Indian Academy of Sciences – Section A,</em> started  in 1934, the journal evolved into an independent journal titled ‘Proceedings – Chemical Sciences’ in 1978 . It was retitled ‘Journal of Chemical Sciences’ in 2004.</p>
            <h3>Aim and Scope</h3>
            <p>The journal publishes original research articles and rapid communications covering all areas of chemical sciences.  It also publishes special issues of conference proceedings/symposia on frontier areas of the subject.</p>
